//! Numeric Operations
//! ------------------

fn main() {

    //addition
    let sum = 4 + 10;

    //subtraction
    let difference = 95.5 - 4.3;

    //multiplication
    let product = 4 * 30;

    //division
    let quotient = 56.7 / 32.2;
    let truncated = -5 / 3; // results in -1

    // remainder
    let remainder = 43 % 5;

    println!("Sum: {sum}\nDifference: {difference}\nProduct: {product}");
    println!("Quotient: {quotient}\nTruncated: {truncated}\nRemainder: {remainder}");
}